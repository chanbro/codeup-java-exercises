public class StringExercise {
    public static void main(String[] args) {

        String pinkFloyd = "We don't need no education\nWe don't need no thought control\n";
        String quotes ="Check \"this\" out!, \"s inside of \"s!\n";
        String driveC = "In windows, the main drive is usually C:\\\n";
        String backslash = "I can do backslashes \\, double backslashes \\\\,\nand the amazing triple backslash \\\\\\!\n";
        System.out.println(pinkFloyd);
        System.out.println(quotes);
        System.out.println(driveC);
        System.out.println(backslash);
    }
}

